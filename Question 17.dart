// 17. Write Program use switch statement. Display Monday to Sunday

void main(List<String> args) {
  var ch;
  print('show monday to sunday');
  switch (ch) {
    case 1:
      print('monday');
    case 2:
      print('tuesday');
    case 3:
      print('wednesday');
    case 4:
      print('thursday');
    case 5:
      print('friday');
    case 6:
      print('saturday');
    case 7:
      print('sunday');
  }
}
